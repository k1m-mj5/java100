public class Java100_variable_DataType2 {
	public static void main(String[] args) {
		
		byte b = 127;
		short s = 32767;
		int i = 2147483647;
		long l = 7000000000L;
		float f = 9.8F;
		double d = 3.14;
		char c = 'A';
		boolean bl = false;
		
		System.out.println(b+"byte MAX-->"+Byte.MAX_VALUE);
		System.out.println(s+"short MAX-->"+Short.MAX_VALUE);
		System.out.println(i+"int MAX-->"+Integer.MAX_VALUE);
		System.out.println(l);
		System.out.println(f);
		System.out.println(d);
		System.out.println(c);
		System.out.println(bl);
		
	}
}